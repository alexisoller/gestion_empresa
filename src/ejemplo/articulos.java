    private void crearConexion() {
        try {
            
            stmt = conn.createStatement(ResultSet.TYPE_SCROLL_SENSITIVE, ResultSet.CONCUR_UPDATABLE);
            cargarcombo();
            String sql = "select * from articulos";
            rs = stmt.executeQuery(sql);
            System.out.println(rs);
            if (rs.first()) {
                mostrarDatos(rs);
            }
            rs.last();
            total = rs.getRow();
            lbtotal.setText(String.valueOf(total));
            rs.first();
            lbrgactual.setText(String.valueOf(rs.getRow()));
            
        } catch (SQLException sqle) {
            mostrarError(sqle.getMessage());
        }
    }
    
    private void mostrarDatos(ResultSet rs) throws SQLException {
        System.out.println("Mostrando..." + rs.getRow());
        if (rs.getRow() > 0) {
            tfid.setText(rs.getLong(1) + "");
            tfnombre.setText(rs.getString(2));
            tfprecio.setText(String.valueOf(rs.getFloat(3)));
            tfcodigo.setText(rs.getString(4));
            //forma rudimentaria para mostrar el item correcto este bucle pasa por todos los elementos del combo box y compara
            // si contiene o no el numero que tenemos en la base de datos puesto que trabaja con equals compara el id tal cual esta
            for (int i = 0; i < cbgrupo.getItemCount(); i++) {
                cbgrupo.setSelectedIndex(i);
                String palabra = String.valueOf(cbgrupo.getSelectedItem());
                String[] iddesc = palabra.split("-");
                if (iddesc[0].equals(String.valueOf(rs.getInt(5)))) {
                    cbgrupo.setSelectedItem(i);
                    break;
                }
            }
            
        } else {
            tfid.setText("");
            tfnombre.setText("");
            tfprecio.setText("");
            tfcodigo.setText("");
            cbgrupo.setSelectedItem(1);
        }
        btnPrime.setEnabled(!rs.isFirst());
        btnAnt.setEnabled(!rs.isFirst());
        btnUltim.setEnabled(!rs.isLast());
        btnSig.setEnabled(!rs.isLast());
        
    }
    
    private void mostrarError(String msg) {
        System.out.println(msg);
        JOptionPane.showMessageDialog(this, msg, "Error", JOptionPane.ERROR_MESSAGE);
    }
    
    private void cargarcombo() throws SQLException {
        String sql = "SELECT * FROM empresa_ad.grupos;";
        rs = stmt.executeQuery(sql);
        
        while (rs.next()) {
            String gr = rs.getLong(1) + "-" + rs.getString(2);
            cbgrupo.addItem(gr);
        }
    }
    
