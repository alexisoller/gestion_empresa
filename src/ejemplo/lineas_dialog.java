    private void jbcalcularimporteActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jbcalcularimporteActionPerformed
      if (jtfcantidad.getText().length() != 0 && this.isNumeric(jtfcantidad.getText())) {

            jtfimporte.setText(String.valueOf(Integer.parseInt(lbprecioarticulo.getText()) * Integer.parseInt(jtfcantidad.getText())));

            jbguardar.setEnabled(true);
        }else{
        JOptionPane.showMessageDialog(this,
    "ERROR: CAMPO CANTIDAD VACIO O CON SIMBOLOS NO ACEPTADOS (solo aptos numeros)",
    "ERROR",JOptionPane.ERROR_MESSAGE);
         jbguardar.setEnabled(false);
        jtfcantidad.requestFocus(true);
        }
    }//GEN-LAST:event_jbcalcularimporteActionPerformed

