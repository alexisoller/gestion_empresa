    private void jbguardarActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jbguardarActionPerformed
        try {

            if (editaroanadir) {
                pstmtanadirlineas.setInt(1, Integer.parseInt(jtflinea.getText()));
                pstmtanadirlineas.setInt(2, Integer.parseInt(jtffactura.getText()));
                String[] iddesc = String.valueOf(cbarticulos.getSelectedItem()).split("-");
                
                pstmtanadirlineas.setInt(3, Integer.parseInt(iddesc[0]));
                pstmtanadirlineas.setInt(4, Integer.parseInt(jtfcantidad.getText()));
                pstmtanadirlineas.setInt(5, Integer.parseInt(jtfimporte.getText()));
                pstmtanadirlineas.executeUpdate();
                jtflinea.setText(String.valueOf(Integer.parseInt(jtflinea.getText()) + 1));
                jtfcantidad.setText("");
                jtfimporte.setText("");
                JOptionPane.showMessageDialog(this,"REGISTRO CORRECTAMENTE");

            } else {
                
                String[] iddesc = String.valueOf(cbarticulos.getSelectedItem()).split("-");
                
               
                
                String sql="UPDATE `empresa_ad`.`lineas_factura` SET `articulo` ="+Integer.parseInt(iddesc[0])+",`cantidad` = "+Integer.parseInt(jtfcantidad.getText())+",`importe` = "+Integer.parseInt(jtfimporte.getText())+" WHERE `linea` = "+Integer.parseInt(jtflinea.getText())+" AND `factura` = "+Integer.parseInt(jtffactura.getText());
                stmt = conn.createStatement(ResultSet.TYPE_SCROLL_SENSITIVE, ResultSet.CONCUR_UPDATABLE);
                stmt.executeUpdate(sql);
                
               JOptionPane.showMessageDialog(this,"REGISTRO EDITADO CORRECTAMENTE");
                
                this.dispose();
                
               
                
                
                
                
                
                
                
            }
        } catch (SQLException ex) {
            Logger.getLogger(lineas_dialog.class.getName()).log(Level.SEVERE, null, ex);
        }
    }//GEN-LAST:event_jbguardarActionPerformed

