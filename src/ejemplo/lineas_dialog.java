    public void ponerdatoseditar(String linea, String factura, String idarti,String cantidad, String importe) {
        jtflinea.setText(linea);
        jtffactura.setText(factura);
        jtfcantidad.setText(cantidad);
        jtfimporte.setText(importe);

        for (int i = 0; i < cbarticulos.getItemCount(); i++) {
            cbarticulos.setSelectedIndex(i);
            String palabra = String.valueOf(cbarticulos.getSelectedItem());
            String[] iddesc = palabra.split("-");
            if (iddesc[0].equals(idarti)) {
                cbarticulos.setSelectedItem(i);
                break;
            }
        }
        editaroanadir = false;
      
    }

